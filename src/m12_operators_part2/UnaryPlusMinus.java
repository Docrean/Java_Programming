package m12_operators_part2;

public class UnaryPlusMinus {

    public static void main(String[] args) {

        int a = +5; //optional to give the + as by default numbers are positive to console unless specified
        System.out.println(a);
        int b = +a;
        System.out.println(b);

        int x = 10;

        int y = -x + 5;

        System.out.println(y); //prints -5 which denotes a number less than 0






    }

}
