package m11_operators_part1;

public class OperatorAssignment {

    public static void main(String[] args) {

        int chairs = 40; //declaration and initialization
        System.out.println(chairs + 2);//42 just printing NOT assigned
        int moreChairs = chairs + 5;//add 5 to chairs' value and assign it to moreChairs
        System.out.println(moreChairs); //45. arithmetic operators don't change variable value
        //only time variable value changes is if reassigned


        //Applying arithmetic operation alone won't change the value of the variable
    }
}
