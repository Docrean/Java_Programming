package day01_IntroToProgramming;

public class PrintVariable1 {
    public static void main(String[] args) {

        int count = 500;
        int backCount = 400;

        double age = 40; //Implicit casting byte-->short-->int-->long-->float-->double
        char gender ='M'; //Single Character

        boolean IsRetired = false;
        boolean IsNotRetired = true;

        System.out.println(count);
        System.out.println(backCount);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(IsRetired);
        System.out.println(IsNotRetired);
    }
}
